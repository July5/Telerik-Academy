using System;


class FiveVariables
{
    static void Main()
    {
        sbyte variable1 = -115;
        byte variable2 = 97;
        ushort variable3 = 52130;
        int variable4 = 4825932;
        short variable5 = -10000;
        Console.WriteLine("{0} is variable of type sbyte.", variable1);
        Console.WriteLine("{0} is variable of type byte.", variable2);
        Console.WriteLine("{0} is variable of type ushort.", variable3);
        Console.WriteLine("{0} is variable of type integer.", variable4);
        Console.WriteLine("{0} is variable of type short.", variable5);
    }
}
