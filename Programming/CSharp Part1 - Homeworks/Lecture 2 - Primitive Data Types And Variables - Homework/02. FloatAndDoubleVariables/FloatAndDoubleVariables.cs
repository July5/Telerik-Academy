using System;

class FloatAndDoubleVariables
{
    static void Main()
    {
        float variable1 = 12.345f;
        float variable2 = 3456.091f;
        double variable3 = 34.567839023;
        double variable4 = 8923.1234857;
        Console.WriteLine("{0} is variable of type float.",variable1);
        Console.WriteLine("{0} is variable of type float.", variable2);
        Console.WriteLine("{0} is variable of type double.", variable3);
        Console.WriteLine("{0} is variable of type double.", variable4);
    }
}

